package loop2;
import java.lang.*;
public class Test01 {
	public static void main (String[] args) {
		
		for (int i = 1; i<=10; i++) {
		System.out.println("for="+ i);
		}
		
		
		//while 반복문
		
		int n = 1;
		while (n<=10) {
			System.out.println("while = " + n);
			n++; 
		}
	}
}
