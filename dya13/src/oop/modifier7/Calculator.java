package oop.modifier7;

//객체를 생성하지않아도 이용 가능하도록 고정(Static)

public class Calculator {

		
	// 고정 되었으므로 객체없이 사용이 가능.
			
		public static int Plus(int first, int second) {
			return first + second;
		}
		public static int Minus(int first, int second) {
			return first - second;
		}
		
		
		
		
		
		
	}
